export function getDynamicTaunt(stage, topType, flags) {
  const majorFlag = Object.entries(flags)
    .sort((a, b) => b[1] - a[1])[0]

  if (majorFlag && majorFlag[1] >= (stage === 0 ? 3 : 4) && FLAG_TAUNTS[majorFlag[0]]) {
    return FLAG_TAUNTS[majorFlag[0]][stage]
  }

  return TYPE_TAUNTS[topType]?.[stage] ?? {
    title: stage === 0 ? '后台先记你一笔' : '系统继续嘴你',
    body: '你前面的答案已经暴露出不少东西了。先别急，后面系统还会继续顺着你的话把你自己供出来。',
  }
}

